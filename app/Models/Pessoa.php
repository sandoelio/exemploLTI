<?php

namespace App\Models;

use Eloquent as Model;
use Illuminate\Database\Eloquent\SoftDeletes;

/**
 * Class Pessoa
 * @package App\Models
 * @version January 3, 2020, 2:44 pm UTC
 *
 * @property string nome
 * @property string cpf
